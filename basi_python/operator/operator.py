# Examples of Arithmetic Operator 
a = 90
b = 4

# Addition of numbers 
add = a + b 
# Subtraction of numbers 
sub = a - b 
# Multiplication of number 
mul = a * b 
# Division(float) of number 
div1 = a / b 
# Division(floor) of number 
div2 = a // b 
# Modulo of both number 
mod = a % b 

# print results 
print("ADDTION=",add) 
print("SUB=",sub) 
print("MULTI=",mul) 
print("DIV=",div1) 
print("DIV2=",div2) 
print("MOD_VALUE=",mod) 
